#include "../aes.h"
#include "../aes-gcm.h"
#include <stdio.h>
#include <string.h>

/*
 * Tests the aes_gcm_* functions with the example values in
 * https://csrc.nist.gov/CSRC/media/Projects/Cryptographic-Standards-and-Guidelines/documents/examples/AES_GCM.pdf
 */
int main(int argc, char **argv) {
  /* Values that are common to all examples. */
  const unsigned char key[16] = {
    0xfe,0xff,0xe9,0x92,0x86,0x65,0x73,0x1c,0x6d,0x6a,0x8f,0x94,0x67,0x30,0x83,0x08
  };
  const unsigned char iv[12] = {
    0xca,0xfe,0xba,0xbe,0xfa,0xce,0xdb,0xad,0xde,0xca,0xf8,0x88
  };
  const unsigned char plaintext[64] = {
    0xd9,0x31,0x32,0x25,0xf8,0x84,0x06,0xe5,0xa5,0x59,0x09,0xc5,0xaf,0xf5,0x26,0x9a,
    0x86,0xa7,0xa9,0x53,0x15,0x34,0xf7,0xda,0x2e,0x4c,0x30,0x3d,0x8a,0x31,0x8a,0x72,
    0x1c,0x3c,0x0c,0x95,0x95,0x68,0x09,0x53,0x2f,0xcf,0x0e,0x24,0x49,0xa6,0xb5,0x25,
    0xb1,0x6a,0xed,0xf5,0xaa,0x0d,0xe6,0x57,0xba,0x63,0x7b,0x39,0x1a,0xaf,0xd2,0x55
  };
  const unsigned char aad[64] = {
    0x3a,0xd7,0x7b,0xb4,0x0d,0x7a,0x36,0x60,0xa8,0x9e,0xca,0xf3,0x24,0x66,0xef,0x97,
    0xf5,0xd3,0xd5,0x85,0x03,0xb9,0x69,0x9d,0xe7,0x85,0x89,0x5a,0x96,0xfd,0xba,0xaf,
    0x43,0xb1,0xcd,0x7f,0x59,0x8e,0xce,0x23,0x88,0x1b,0x00,0xe3,0xed,0x03,0x06,0x88,
    0x7b,0x0c,0x78,0x5e,0x27,0xe8,0xad,0x3f,0x82,0x23,0x20,0x71,0x04,0x72,0x5d,0xd4
  };
  const unsigned char ciphertext[64] = {
    0x42,0x83,0x1e,0xc2,0x21,0x77,0x74,0x24,0x4b,0x72,0x21,0xb7,0x84,0xd0,0xd4,0x9c,
    0xe3,0xaa,0x21,0x2f,0x2c,0x02,0xa4,0xe0,0x35,0xc1,0x7e,0x23,0x29,0xac,0xa1,0x2e,
    0x21,0xd5,0x14,0xb2,0x54,0x66,0x93,0x1c,0x7d,0x8f,0x6a,0x5a,0xac,0x84,0xaa,0x05,
    0x1b,0xa3,0x0b,0x39,0x6a,0x0a,0xac,0x97,0x3d,0x58,0xe0,0x91,0x47,0x3f,0x59,0x85
  };
  /* Values that are specific to each example. */
  const struct vector {
    unsigned char plaintext_length;
    unsigned char aad_length;
    unsigned char tag_length;
    unsigned char tag[16];
  } vectors[] = {
    { /* Example 1 */
      0, 0, 16, {0x32,0x47,0x18,0x4b,0x3c,0x4f,0x69,0xa4,0x4d,0xbc,0xd2,0x28,0x87,0xbb,0xb4,0x18}
    },{ /* Example 2 */
      64, 0, 16, {0x4d,0x5c,0x2a,0xf3,0x27,0xcd,0x64,0xa6,0x2c,0xf3,0x5a,0xbd,0x2b,0xa6,0xfa,0xb4}
    },{ /* Example 3 */
      0, 64, 16, {0x5f,0x91,0xd7,0x71,0x23,0xef,0x5e,0xb9,0x99,0x79,0x13,0x84,0x9b,0x8d,0xc1,0xe9}
    },{ /* Example 4 */
      64, 64, 16, {0x64,0xc0,0x23,0x29,0x04,0xaf,0x39,0x8a,0x5b,0x67,0xc1,0x0b,0x53,0xa5,0x02,0x4d}
    },{ /* Example 5 */
      60, 20, 16, {0xf0,0x7c,0x25,0x28,0xee,0xa2,0xfc,0xa1,0x21,0x1f,0x90,0x5e,0x1b,0x6a,0x88,0x1b}
    },{ /* Example 6 */
      60, 20, 12, {0xf0,0x7c,0x25,0x28,0xee,0xa2,0xfc,0xa1,0x21,0x1f,0x90,0x5e}
    }
  };
  unsigned char text[64];
  unsigned char tag[16];
  unsigned i;

  for (i = 0; i < sizeof(vectors) / sizeof(vectors[0]); i++) {
    const struct vector *v = vectors + i;

    aes_gcm_encrypt(text, tag, iv, plaintext, v->plaintext_length, aad, v->aad_length, key);
    if (memcmp(tag, vectors[i].tag, vectors[i].tag_length)) {
      fprintf(stderr, "aes_gcm_encrypt() tag failed for test vector %u\n", i);
      return 1;
    }
    if (memcmp(text, ciphertext, v->plaintext_length)) {
      fprintf(stderr, "aes_gcm_encrypt() ciphertext failed for test vector %u\n", i);
      return 1;
    }

    if (aes_gcm_decrypt(text, iv, ciphertext, v->plaintext_length, aad, v->aad_length, v->tag, v->tag_length, key)) {
      fprintf(stderr, "aes_gcm_decrypt() tag failed for test vector %u\n", i);
      return 1;
    }
    if (memcmp(text, plaintext, v->plaintext_length)) {
      fprintf(stderr, "aes_gcm_decrypt() plaintext failed for test vector %u\n", i);
      return 1;
    }
  }

  return 0;
}
